e_1=pow(e,e)
e_2=pow(e,e_1)
flag=open("flag.txt",'r').read()
ciphertext=int(pow(bytes_to_long(flag),e_2,N))
print(f"N={N}")
print(f"phi={phi}")
print(f"ciphertext={ciphertext}")

